# ** Оператор elif   только если не срабатывает предыдущий оператор,
# или когда его собственное условие является исстиной
 
var = 'b'

if var == 'A':
    res = "lit A"
# else if иначе если
elif var == 'B':
    res = "lit B"
elif var == 'C':
    res = "lit C"
elif var == 'a':
    res = "lit a"
else:
    res = "не один из вариантов"

