# *********** ПРИМЕР. "Термостат" *************

# текущая температура 
current_temp = 27

# диапазон температур 
min_temp = 21
max_temp = 26

# логика термостата
if current_temp < min_temp:
    print("включен нагрев")
elif current_temp > max_temp:
    print("выключен нагрев")
else:
    print("температура оптимальная")














